def suma(sumando1,sumando2):
    print("resultado de la suma: ", sumando1 + sumando2)
 
def resta(minuendo, sustraendo):
    print("resultado resta: ", minuendo - sustraendo)

def multiplicacion(multiplicando, multiplicador):
    print("resultado de la multiplicación: ", multiplicando * multiplicador)

def potenciacion(base, exponente):
    print("resultado de la potenciación: ", base ** exponente)

